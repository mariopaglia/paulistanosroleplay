			exports["discord-screenshot"]:requestCustomClientScreenshotUploadToDiscord(source,Salas[sala],{encoding = "jpg",quality = 0.1},{content = mensagem},30000,function(error)if error then    return print("^1ERRO: " .. error)end    end)
		else
			print("Falha ao capturar a printscreen!\nO script 'discord-screenshot' não foi encontrado!")
		end
	end
end

function vRP.getWebhook(sala)
	if(sala==nil)then sala= "default" end
	if(Salas[sala]==nil)then
		sala = "default"
	end
	return Salas[sala]
end

