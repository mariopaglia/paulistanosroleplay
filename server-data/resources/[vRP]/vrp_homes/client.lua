-- HOUSETIMER
-----------------------------------------------------------------------------------------------------------------------------------------
Citizen.CreateThread(function()
	while true do
		Citizen.Wait(3000)
		if houseTimer > 0 then
			houseTimer = houseTimer - 3
		end
	end
end)
