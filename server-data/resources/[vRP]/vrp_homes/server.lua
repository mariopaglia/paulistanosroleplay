local copen = {}
local queuel = {}
local queuer = {}

function count(tbl)
	local a = 0
	if type(tbl) == "table" then
		for k, v in pairs(tbl) do
			a = a+1
		end
	end
	return a
end

function queue(f, name)
	local r = async()
	async(function()
		local ids = idgens:gen()
		queuel[ids] = {f, ids, name}
		while queuer[ids] == nil do
			Citizen.Wait(5)
		end
		local x = queuer[ids]
		queuer[ids] = nil
		idgens:free(ids)
		r(x)
	end)
	
	return r:wait()
end

Citizen.CreateThread(function()
	while true do
		Citizen.Wait(1)
		if count(queuel) > 0 then
			for k, v in pairs(queuel) do
				local x = v
				queuel[v[2]] = nil
				Citizen.CreateThread(function()
					queuer[x[2]] = x[1]()
				end)
			end
		end
	end
end)


